void solve(){
 ll n;
 cin>>n;
 vector<ll> v(n);
 inpv(v);

 ll neg=0,pos=0;
 fr(i,n){
    if(v[i]==1)pos++;
    else neg++;
 }

 if(neg<=pos){
    if(neg%2){
        cout<<1<<endl;
        return;
    }
    else{
        cout<<0<<endl;
        return;
    }
 }

 ll diff=neg-pos;
 ll ans=0;

 if(diff%2)
  ans=(diff/2)+1;
 else 
  ans=diff/2;

 neg=neg-ans;
 pos=pos+ans;

 if(abs(neg)%2==0){
    cout<<ans<<endl;
    return;
 }else{
    cout<<ans+1<<endl;
    return;
 }

}


