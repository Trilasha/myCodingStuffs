void solve(){
    ll n;
    cin>>n;
    vector<ll> v(n);
    inpv(v);
    vector<pll> vp={{0,1},{1,2},{0,2},{0,0},{1,1},{2,2},{1,0},{2,1},{2,0}};
    ll ans=1e18;
    for(ll i=0;i<9;++i){
        vector<ll> temp=v;
        ll sum=0,add=0;
        temp[0]+=vp[i].ff;
        temp[1]+=vp[i].ss;
        add+=vp[i].ff+vp[i].ss;
        sum+=temp[0]+temp[1]+temp[2];
        add+=sum%3;
        temp[2]+=add;
        for(ll j=3;j<n;++j){
            sum-=temp[j-3];
            sum+=temp[j];
            ll rem=sum%3;
            add+=rem;
            temp[j]+=rem;
        }
        ans=min(ans,add);
    }
    cout<<ans<<endl;
}


int main(){

fast_io;

ll q=1;
cin>>q;
for(ll i=0;i<q;i++){
    solve();
}
    return 0;
}






