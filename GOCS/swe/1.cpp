void solve(){
    ll n,k;
    cin>>n>>k;
    string s;
    cin>>s;
    map<ll,ll> mp;
    for(ll i=0;i<n;++i){
        mp[s[i]]++;
    }
    
    nCr comb(N,modval);

    ll ans=0;
    for(ll i=0;i<n;++i){
        ll curr=mp.size();
        if(curr>=(k-1)){
            ans=(ans+comb.C(curr-1,k-1))%modval;
        }
        mp[s[i]]--;
        if(mp[s[i]]==0){
            mp.erase(s[i]);
        }
    }

    cout<<ans<<endl;

}


